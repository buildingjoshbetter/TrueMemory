"""MEMORIST Phase 9 sweep driver.

Iterates a candidate × dataset matrix, calling `run_candidate.py` for each
combination, and emits a summary table at the end. Kept separate from
`run_candidate.py` so individual runs are still invokable without
running the full sweep.

Usage:
    # Run every discovered candidate against both datasets
    python benchmarks/gate_eval/run_sweep.py --all

    # Run a specific subset
    python benchmarks/gate_eval/run_sweep.py \\
        --candidates v05_baseline_nogate v05_gate_threshold \\
        --datasets short_horizon_200

    # Threshold-sweep one parameterized candidate
    python benchmarks/gate_eval/run_sweep.py \\
        --candidate v05_gate_threshold \\
        --thresholds 0.10 0.20 0.30 0.40 0.50 \\
        --dataset short_horizon_200

Output:
    benchmarks/gate_eval/results/<candidate>__<dataset>.json (one per cell)
    benchmarks/gate_eval/results/SWEEP_SUMMARY.md (rebuilt every run)
"""

from __future__ import annotations

import argparse
import json
import subprocess
import sys
import time
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[2]
RESULTS_DIR = REPO_ROOT / "benchmarks" / "gate_eval" / "results"

DEFAULT_DATASETS = ["short_horizon_200", "long_horizon_synthetic"]


def discover_candidate_names() -> list[str]:
    """Re-uses run_candidate's discover_candidates by importing it."""
    sys.path.insert(0, str(REPO_ROOT))
    from benchmarks.gate_eval.run_candidate import discover_candidates
    return sorted(discover_candidates().keys())


def run_one(candidate: str, dataset: str, output: Path,
            extra_env: dict | None = None) -> tuple[bool, str]:
    """Run a single (candidate, dataset) cell. Returns (success, message)."""
    env_args = []
    if extra_env:
        for k, v in extra_env.items():
            env_args.extend(["env", f"{k}={v}"])

    cmd = [
        sys.executable,
        str(REPO_ROOT / "benchmarks" / "gate_eval" / "run_candidate.py"),
        "--candidate", candidate,
        "--dataset", dataset,
        "--output", str(output),
    ]

    # Ensure the bad ANTHROPIC_API_KEY doesn't poison child processes
    import os
    child_env = {k: v for k, v in os.environ.items() if k != "ANTHROPIC_API_KEY"}
    if extra_env:
        child_env.update(extra_env)

    try:
        proc = subprocess.run(
            cmd, capture_output=True, text=True, timeout=1800, env=child_env,
        )
    except subprocess.TimeoutExpired:
        return False, "timeout (>30 min)"

    if proc.returncode != 0:
        return False, f"exit {proc.returncode}: {proc.stderr.strip()[:200]}"
    return True, proc.stdout.strip().splitlines()[-1] if proc.stdout else "ok"


def write_summary_md(results_dir: Path) -> Path:
    """Walk results_dir/*.json and write a single SWEEP_SUMMARY.md."""
    rows: list[dict] = []
    for jf in sorted(results_dir.glob("*.json")):
        try:
            data = json.loads(jf.read_text())
        except json.JSONDecodeError:
            continue
        if not isinstance(data, dict) or "candidate" not in data:
            continue
        rows.append({
            "candidate": data["candidate"],
            "dataset": data["dataset"],
            "precision_at_k_pct": data.get("retrieval", {}).get("precision_at_k_pct", 0),
            "kept": data.get("ingest", {}).get("kept", 0),
            "messages": data.get("ingest", {}).get("total_messages", 0),
            "drop_rate_pct": data.get("ingest", {}).get("drop_rate_pct", 0),
            "ingest_s": data.get("ingest", {}).get("ingest_wall_clock_s", 0),
            "db_bytes": data.get("storage", {}).get("db_size_bytes", 0),
            "rows": data.get("storage", {}).get("rows_in_messages", 0),
            "ts": data.get("timestamp", ""),
        })

    rows.sort(key=lambda r: (r["dataset"], r["candidate"]))

    out = results_dir / "SWEEP_SUMMARY.md"
    lines = [
        "# MEMORIST Phase 9 — Sweep Summary",
        "",
        f"Auto-generated by `run_sweep.py`. Last update: {time.strftime('%Y-%m-%d %H:%M:%S')}.",
        "",
        "| Candidate | Dataset | precision@k % | Kept / Msgs | Drop % | Ingest s | DB bytes | Rows |",
        "|---|---|---:|---|---:|---:|---:|---:|",
    ]
    for r in rows:
        lines.append(
            f"| {r['candidate']} | {r['dataset']} | {r['precision_at_k_pct']} "
            f"| {r['kept']} / {r['messages']} | {r['drop_rate_pct']} "
            f"| {r['ingest_s']} | {r['db_bytes']} | {r['rows']} |"
        )

    out.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return out


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--all", action="store_true",
                        help="Run every discovered candidate against both datasets")
    parser.add_argument("--candidate", help="Single candidate (alternative to --candidates)")
    parser.add_argument("--candidates", nargs="*", help="Subset of candidates")
    parser.add_argument("--dataset", help="Single dataset")
    parser.add_argument("--datasets", nargs="*", default=DEFAULT_DATASETS, help="Subset of datasets")
    parser.add_argument("--thresholds", nargs="*", type=float,
                        help="Threshold sweep values (only for v05_gate_threshold)")
    parser.add_argument("--summary-only", action="store_true",
                        help="Skip running; just rebuild SWEEP_SUMMARY.md from existing results")
    args = parser.parse_args()

    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    if args.summary_only:
        out = write_summary_md(RESULTS_DIR)
        print(f"Wrote {out}")
        return

    if args.all:
        candidates = discover_candidate_names()
        datasets = list(DEFAULT_DATASETS)
    else:
        candidates = args.candidates or ([args.candidate] if args.candidate else discover_candidate_names())
        datasets = [args.dataset] if args.dataset else list(args.datasets)

    cells: list[tuple[str, str, Path, dict]] = []
    for cand in candidates:
        for ds in datasets:
            if cand == "v05_gate_threshold" and args.thresholds:
                # Spawn one cell per threshold
                for tau in args.thresholds:
                    out = RESULTS_DIR / f"{cand}_t{tau:.2f}__{ds}.json"
                    cells.append((cand, ds, out, {"GATE_THRESHOLD": str(tau)}))
            else:
                out = RESULTS_DIR / f"{cand}__{ds}.json"
                cells.append((cand, ds, out, {}))

    print(f"Running {len(cells)} cells...", file=sys.stderr)
    failures: list[tuple[str, str, str]] = []
    for i, (cand, ds, out, env) in enumerate(cells, 1):
        print(f"  [{i}/{len(cells)}] {cand} × {ds}", file=sys.stderr)
        ok, msg = run_one(cand, ds, out, env)
        status = "OK" if ok else "FAIL"
        print(f"    {status}: {msg}", file=sys.stderr)
        if not ok:
            failures.append((cand, ds, msg))

    summary_path = write_summary_md(RESULTS_DIR)
    print(f"\nSweep done. Summary: {summary_path}", file=sys.stderr)
    if failures:
        print(f"\n{len(failures)} cell(s) failed:", file=sys.stderr)
        for cand, ds, msg in failures:
            print(f"  - {cand} × {ds}: {msg}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()
